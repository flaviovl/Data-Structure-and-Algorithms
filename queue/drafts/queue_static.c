#include <stdio.h>
#include <stdlib.h>
// #include "fila.h"
#define N 10

//*===----------------------------< Global >----------------------------------===*

static int filaInt[N];
static int p, u;

//*===----------------------------< Funcoes >---------------------------------===*

void criaFila()
{
	p = 0;
	u = 0;
}
//*===------------------------------------------------------------------------===*

int inseriFila(int y)
{
	if (filaCheia())
		return 0;
	filaInt[u++] = y;
	return 1;
}
//*===------------------------------------------------------------------------===*

int removeFila(int *y)
{
	if (filaVazia())
		return 0;
	*y = filaInt[p++];
	return 1;
}
//*===------------------------------------------------------------------------===*

int filaCheia()
{
	return (u == N);
}
//*===------------------------------------------------------------------------===*

int filaVazia()
{
	return (p == u); // true (1) fila vazia
}
//*===------------------------------------------------------------------------===*

int tamanhoFila()
{
	return u + 1;
}
//*===------------------------------------------------------------------------===*

void printFila()
{
	int i, j, k;
	j = (p + 1) * 4 - 1; // posi��o de p = multiplo de 4 - 1
	k = (u - p) * 4 - 1;

	puts("+---------------------------------------+");
	for (i = 0; i < N; i++)

		if (i < u)
			printf("|%3d", filaInt[i]);
		else
			printf("|xxx");

	puts("|");
	puts("+---------------------------------------+");

	for (i = 0; i < j - 1; i++)
		printf(" ");
	printf("p");

	for (i = 0; i < k; i++)
		printf(" ");
	printf("u");

	if (filaVazia())
	{
		puts("\n\n\t+------------------------+");
		puts("\t| --->  Fila Vazia !!    |");
		puts("\t+------------------------+");
	}
	printf("\n  u = %d", u);
	printf("\n  p = %d", p);

	getchar();
}
